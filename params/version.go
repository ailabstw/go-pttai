package params

var TheVersion = ""

// Version holds the textual version string.
var Version = func() string {
	return TheVersion
}()

func VersionWithCommit(gitCommit string) string {
	vsn := Version
	if len(gitCommit) >= 8 {
		vsn += "-" + gitCommit[:8]
	}
	return vsn
}
